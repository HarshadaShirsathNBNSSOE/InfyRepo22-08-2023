package stringprograms;

public class ReverseStringUsingstrBuff {

	public static void main(String[] args)
	{
		StringBuffer ref1 = new StringBuffer("Harshada 123");
		System.out.println(ref1.reverse());
		
		StringBuilder ref2 = new StringBuilder("Shirsath");
		System.out.println(ref2.reverse());
	}
	
}
